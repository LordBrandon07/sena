def suma (a,b):  #funcion con dos  parametros llamada suma
    return a+b #retorna una suma entre los dos parametros

def suma(a,b,c):  #funcion con tres parametros tambien llamada suma
    return a+b+c  #retorna la suma entre los 3 paramtros

print(suma(1,2,3))  #imprime la funcion que necesita tres parametros por la sobreescritura